package com.example.javaproject2.week2.day4;

public class BitOperatorEx2 {
    public static void main(String[] args) {
        int iVal1 = 3;
        System.out.println(~iVal1);
        System.out.println(Integer.toBinaryString(~iVal1));
        System.out.println(Integer.toBinaryString(iVal1));
    }
}
