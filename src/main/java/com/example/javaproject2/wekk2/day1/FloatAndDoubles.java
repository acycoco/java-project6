package com.example.javaproject2.wekk2.day1;

public class FloatAndDoubles {
    public static void main(String[] args) {

        float f5 = 1.23e5f;
        float f6 = 1.23e6f;
        float f7 = 1.23e7f;
        float f8 = 1.23e8f;
        float f9 = 1.23e9f;
        float f10 = 1.23e10f;
        double d10 = 1.23e10;

        System.out.println("f5 = " + f5);
        System.out.println("f6 = " + f6);
        System.out.println("f7 = " + f7);
        System.out.println("f8 = " + f8);
        System.out.println("f9 = " + f9);
        System.out.println("f10 = " + f10);
        System.out.println("d10 = " + d10);
        float f11 = 3.1415922123424545315f;
        System.out.println("f11 = " + f11);


        float f1 = 3.14f;
        float f2 = 1.23e10f;

        double d1 = 3.141592653589793;
        double d2 = 1.23e100;

        float result1 = f1 * 2.0f;
        double result2 = d1 / 2.0;

        System.out.println("f1 = " + f1);
        System.out.println("f2 = " + f2);
        System.out.println("d1 = " + d1);
        System.out.println("d2 = " + d2);
        System.out.println("result1 = " + result1);
        System.out.println("result2 = " + result2);
    }
}
